function heart() {
  var x = document.getElementById('heart');
  var y = document.getElementById('liked');

  if(x.style.color=="red"){
    x.style.color="black";
    y.style.opacity="0";

  }
  else{
    x.style.color="red";
    y.style.animation="liked 0.2s forwards linear";

  }

}
